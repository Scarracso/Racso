    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String fd = sdf.format(tanggal.getValue());
        

        String zsql = "insert into data_peminjaman values (?,?,?,?,?,?,?,?,?)";
        try{
            int t = Table.getRowCount();
            for(int i=0; i < t ; i++)
            {
                String Tanggal_Peminjaman = Table.getValueAt(i, 0).toString();
                String Tanggal_Pengembalian = Table.getValueAt(i, 1).toString();
                String Jumlah_pinjaman = Table.getValueAt(i, 2).toString();
                String Nomor_Buku = Table.getValueAt(i, 3).toString();
                String Judul_Buku = Table.getValueAt(i, 4).toString();

                PreparedStatement stat2 = conn.prepareStatement(zsql);
                stat2.setString(1, fd);
                stat2.setString(2, no_pinjam.getText());
                stat2.setString(3, Tanggal_Peminjaman);
                stat2.setString(4, Tanggal_Pengembalian);
                stat2.setString(5, Jumlah_pinjaman);
                stat2.setString(6, Nomor_Buku);
                stat2.setString(7, Judul_Buku);
                stat2.setString(8, no_ang.getText());
                stat2.setString(9, nama_ang.getText());
                stat2.executeUpdate();
            }
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
        }catch(Exception e)
        {
            System.out.println("Error : "+e);
        }
        kosong();
        autonumber();
        removeTable();
                                  
    }//GEN-LAST:event_SimpanActionPerformed

    private void TambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
String pinjam = sdf.format(tglpinjam.getDate());
String kembali = sdf.format(tglkembali.getDate());
        try{
            String pinjam_buku = pinjam;
            String kembalibuku = kembali;
            String juml = jml_pinjam.getText();
            String nobuk = no_buku.getText();
            String jdlbuk = jdl_buku.getText();
            

            tabmode.addRow(new Object[]{pinjam_buku,kembalibuku,juml,nobuk,jdlbuk});
            Table.setModel(tabmode);
        }
        catch(Exception e)
        {
            System.out.println("Error : "+e);
        }
        tglpinjam.setCalendar(null);
        tglkembali.setCalendar(null);
        jml_pinjam.setText("");
        no_buku.setText("");
        jdl_buku.setText("");
    }//GEN-LAST:event_TambahActionPerformed

