protected void autonumber(){
 try{
 String sql = "SELECT No_pinjam FROM data_peminjaman order by No_pinjam asc";
 Statement stat = conn.createStatement();
 ResultSet rs=stat.executeQuery(sql);

 while(rs.next()){
 String No_pinjam = rs.getString("No_pinjam").substring(2);
 int nomor = Integer.parseInt(No_pinjam) + 1;
 String nol = "";

 if (nomor<10)
 {nol = "000";}
 else if (nomor<100)
 {nol = "00";}
 else if (nomor<1000)
 {nol = "";}

 no_pinjam.setText("P" + nol + nomor);
 }
 }
 catch (Exception e){
      JOptionPane.showMessageDialog(null,"Penomoran Salah" + e);
 }
}

